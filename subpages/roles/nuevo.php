function volver(){
	window.location = '?modulo=listarol';
}

function guardar(){
	var nombre = $('#nombre').val();
	if(nombre == ''){
		$('#blanco').modal('show');
	}else{
		$('#btnguardar').fadeOut('slow');
		$('#wait').delay(600).fadeIn('slow');
		$.post('subpages/roles/ajax/guardarnuevo.php',{
			nombre : nombre
		}).done(function(response){
			if($.trim(response) == 'ok'){
				$('#guardado').modal('show');
			}
		});
	}
}
</script>